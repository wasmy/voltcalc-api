# 4.  Endpoint 1 — POST /api/calculate-tco
# ---------------------------------------------------------------------------

@app.post(
    "/api/calculate-tco",
    response_model=TCOResponse,
    summary="Calculate Total Cost of Ownership",
    tags=["TCO"],
)
def calculate_tco(payload: TCORequest) -> TCOResponse:
    """
    Compare monthly and yearly fuel costs between an ICE vehicle and an EV.

    **Formula:**
    - Daily gas cost  = (daily_mileage / gas_mpg) × gas_price_per_gallon
    - Daily EV cost   = (daily_mileage / ev_miles_per_kwh) × electricity_rate_per_kwh
    - Monthly X       = daily X × 30.44  (average days / month)
    - Yearly savings  = (monthly_gas_cost − monthly_ev_cost) × 12
    """
    # Average days per month (more accurate than a fixed 30)
    DAYS_PER_MONTH: float = 30.44

    # --- Daily fuel costs ---
    daily_gas_cost: float = (payload.daily_mileage / payload.gas_mpg) * payload.gas_price_per_gallon
    daily_ev_cost: float = (payload.daily_mileage / payload.ev_miles_per_kwh) * payload.electricity_rate_per_kwh

    # --- Scale to monthly ---
    monthly_gas_cost: float = round(daily_gas_cost * DAYS_PER_MONTH, 2)
    monthly_ev_cost: float = round(daily_ev_cost * DAYS_PER_MONTH, 2)

    # --- Yearly savings (positive = EV wins, negative = ICE cheaper) ---
    yearly_savings: float = round((monthly_gas_cost - monthly_ev_cost) * 12, 2)

    return TCOResponse(
        daily_gas_cost=round(daily_gas_cost, 2),
        daily_ev_cost=round(daily_ev_cost, 2),
        monthly_gas_cost=monthly_gas_cost,
        monthly_ev_cost=monthly_ev_cost,
        yearly_savings=yearly_savings,
    )


