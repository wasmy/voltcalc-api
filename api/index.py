# 3.  Pydantic request / response models
# ---------------------------------------------------------------------------

class TCORequest(BaseModel):
    """Input payload for the Total Cost of Ownership calculation."""

    daily_mileage: float = Field(
        ..., gt=0, le=1500,
        description="Miles driven per day (must be positive).",
        examples=[40],
    )
    gas_price_per_gallon: float = Field(
        ..., gt=0, le=30,
        description="Current retail price of gasoline per gallon (USD or local).",
        examples=[3.80],
    )
    gas_mpg: float = Field(
        ..., gt=0, le=200,
        description="Fuel efficiency of the equivalent ICE vehicle in MPG.",
        examples=[30],
    )
    electricity_rate_per_kwh: float = Field(
        ..., gt=0, le=5,
        description="Electricity tariff per kWh (USD or local).",
        examples=[0.13],
    )
    ev_miles_per_kwh: float = Field(
        ..., gt=0, le=10,
        description="EV efficiency: miles driven per kWh consumed.",
        examples=[3.5],
    )


class TCOResponse(BaseModel):
    """Calculated cost comparison between ICE and EV."""

    monthly_gas_cost: float = Field(description="Estimated monthly fuel cost for an ICE vehicle.")
    monthly_ev_cost: float = Field(description="Estimated monthly electricity cost for an EV.")
    yearly_savings: float = Field(description="Annual money saved by driving an EV vs. ICE.")
    daily_gas_cost: float
    daily_ev_cost: float


class SubsidyItem(BaseModel):
    """A single government EV incentive / subsidy."""

    subsidy_name: str
    amount: float
    currency: str
    type: str
    notes: str


class SubsidyResponse(BaseModel):
    country: str
    region: str
    subsidies: list[SubsidyItem]
    total_potential_value: float = Field(
        description="Sum of all monetary subsidies (excludes non-monetary benefits)."
    )


class InsuranceRequest(BaseModel):
    """Input payload for the insurance premium estimator."""

    country: Literal["us", "canada", "australia", "new zealand", "uk", "other"] = Field(
        ..., description="ISO-style country key (lowercase).",
        examples=["us"],
    )
    ev_purchase_price: float = Field(
        ..., gt=5_000, le=500_000,
        description="Retail purchase price of the EV.",
        examples=[42000],
    )
    driver_age: int = Field(
        ..., ge=16, le=100,
        description="Primary driver's age in years.",
        examples=[35],
    )

    @field_validator("country", mode="before")
    @classmethod
    def normalise_country(cls, v: str) -> str:
        """Accept any casing; store lowercase."""
        return v.strip().lower()


class InsuranceResponse(BaseModel):
    """Estimated insurance premium breakdown."""

    estimated_monthly_premium: float = Field(description="Estimated monthly insurance premium.")
    estimated_annual_premium: float
    currency: str
    risk_profile: str = Field(description="Low / Medium / High — summarises the risk tier.")
    disclaimer: str


