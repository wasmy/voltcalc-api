# 5.  Endpoint 2 — GET /api/subsidies/{country}/{region}
# ---------------------------------------------------------------------------

@app.get(
    "/api/subsidies/{country}/{region}",
    response_model=SubsidyResponse,
    summary="List EV subsidies for a country / region",
    tags=["Subsidies"],
)
def get_subsidies(
    country: str = Path(..., description="Country name (e.g. 'us', 'canada', 'australia', 'new zealand')"),
    region: str = Path(..., description="State / province / region (e.g. 'california', 'ontario')"),
) -> SubsidyResponse:
    """
    Returns all applicable EV subsidies for the given **country** and **region**.

    The lookup merges:
    1. **National / federal** incentives (stored under the `ALL` key).
    2. **Region-specific** incentives (stored under the region's key).

    Both keys are case-insensitive. Unknown locations return an empty list
    rather than a 404 so the UI degrades gracefully.
    """
    country_key: str = country.strip().lower()
    region_key: str = region.strip().lower()

    # Retrieve the country bucket; default to empty dict if not in our database
    country_data: dict[str, list[dict]] = SUBSIDIES.get(country_key, {})

    if not country_data:
        # Country not yet in our database — return empty rather than hard error
        return SubsidyResponse(
            country=country_key,
            region=region_key,
            subsidies=[],
            total_potential_value=0.0,
        )

    # Merge national (ALL) + region-specific incentives
    national: list[dict] = country_data.get("ALL", [])
    regional: list[dict] = country_data.get(region_key, [])
    combined: list[dict] = national + regional

    # Validate each raw dict through the SubsidyItem model (belt-and-suspenders)
    subsidy_items: list[SubsidyItem] = [SubsidyItem(**s) for s in combined]

    # Sum only monetary incentives (skip non_monetary_benefit & tax_exemptions with amount=0)
    total: float = round(
        sum(s.amount for s in subsidy_items if s.amount > 0),
        2,
    )

    return SubsidyResponse(
        country=country_key,
        region=region_key,
        subsidies=subsidy_items,
        total_potential_value=total,
    )


