def _age_multiplier(age: int) -> float:
    """
    Returns a risk multiplier based on driver age.

    Young drivers (<25) and seniors (>70) statistically file more claims.
    This mirrors standard actuarial banding used by major insurers.
    """
    if age < 21:
        return 2.10   # Highest risk tier
    if age < 25:
        return 1.60
    if age < 30:
        return 1.20
    if age < 65:
        return 1.00   # Baseline — experienced adult drivers
    if age < 75:
        return 1.15
    return 1.35        # 75+ — elevated risk


def _price_multiplier(price: float) -> float:
    """
    Higher-value EVs cost more to repair / replace → higher premiums.

    Bracket logic mirrors Comprehensive + Collision coverage tiers:
    - Budget  (<$30k)  → 1.0×
    - Mid     (<$55k)  → 1.25×
    - Premium (<$90k)  → 1.55×
    - Luxury  (≥$90k)  → 1.90×
    """
    if price < 30_000:
        return 1.00
    if price < 55_000:
        return 1.25
    if price < 90_000:
        return 1.55
    return 1.90


def _risk_label(monthly: float) -> str:
    """Bucket the final premium into a human-readable risk profile."""
    if monthly < 100:
        return "Low"
    if monthly < 200:
        return "Medium"
    return "High"


@app.post(
    "/api/estimate-insurance",
    response_model=InsuranceResponse,
    summary="Estimate monthly EV insurance premium",
    tags=["Insurance"],
)
def estimate_insurance(payload: InsuranceRequest) -> InsuranceResponse:
    """
    Fast programmatic insurance estimate using a three-factor model:

    ```
    premium = (base_rate + ev_battery_surcharge)
              × age_multiplier(driver_age)
              × price_multiplier(ev_purchase_price)
    ```

    **Factors:**
    | Factor | Source |
    |--------|--------|
    | `base_rate` | Country-level actuarial baseline |
    | `ev_battery_surcharge` | EV-specific battery replacement risk |
    | `age_multiplier` | Statistical claim frequency by age band |
    | `price_multiplier` | Vehicle value → repair / replacement cost |
    """
    config = COUNTRY_INSURANCE_CONFIG[payload.country]  # country already validated by Pydantic

    # Core calculation — each factor is independently justified above
    base: float = config["base_rate"] + config["ev_battery_surcharge"]
    monthly: float = base * _age_multiplier(payload.driver_age) * _price_multiplier(payload.ev_purchase_price)
    monthly = round(monthly, 2)
    annual: float = round(monthly * 12, 2)

    return InsuranceResponse(
        estimated_monthly_premium=monthly,
        estimated_annual_premium=annual,
        currency=config["currency"],
        risk_profile=_risk_label(monthly),
        disclaimer=(
            "This is a programmatic estimate for informational purposes only. "
            "Actual premiums vary by insurer, driving history, credit score, "
            "coverage type, and local regulations. Always obtain a formal quote."
        ),
    )


